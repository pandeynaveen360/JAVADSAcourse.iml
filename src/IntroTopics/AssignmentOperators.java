package IntroTopics;

public class AssignmentOperators {
    public static void main(String[] args) {

        //assignment operators are used in java to assign value to variable.

        int age;
        age = 4;
        int val = age;
        int a = 20;
        System.out.println(val);

        val += a; //val = val + a

        System.out.println(val);

        val -= a; //val = val - a

        System.out.println(val);

        val *= a; //val = val*a

        System.out.println(val);

        val /= a; //val = val/a

        System.out.println(val);
    }
}
