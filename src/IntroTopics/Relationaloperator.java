package IntroTopics;

public class Relationaloperator {
    public static void main(String[] args) {

        int a = 8, b = 20;
        System.out.println(a + " " + b);

        // == operator (is eqaul to)
        System.out.println(a == b);

        System.out.println(a != b); // not equal to

        System.out.println(a > b); //false

        System.out.println(a < b); //true

        System.out.println(a >= b); //false

        System.out.println(a <= b); //true
    }
}
