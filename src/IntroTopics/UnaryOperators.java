package IntroTopics;

public class UnaryOperators {
    public static void main(String[] args) {

        //unary operators are used with one operand. For eg: ++

        int num = 5;
        System.out.println(++num);

        int a = 12, b = 12;
        int n, m;

        System.out.println(a);

        //increment
        n = ++a;
        System.out.println(n);

        //decrement
        m = --b;
        System.out.println(m);
    }
}
